        static Textures()
        {
            InitTextures(out GrassWall, GrassWallsBytes);
            InitTextures(out StoneWall, StoneWallsBytes);

            InitTextures(out WoodWall, WoodWallsBytes);
            InitTextures(out LeavesWall, LeavesWallsBytes);

            InitTextures(out DirtWall, DirtWallsBytes);
        }
        private static void InitTextures(out Color[] colorArray, byte[] bytesArray)
        {
            int x = 0;
            colorArray = new Color[bytesArray.Length / 3];
            for (int i = 0; i < colorArray.Length; i++)
            {
                colorArray[i] = 
                    Color.FromArgb(
                        bytesArray[x],
                        bytesArray[x + 1],
                        bytesArray[x + 2]
                        );
                x += 3;
            }
        }
